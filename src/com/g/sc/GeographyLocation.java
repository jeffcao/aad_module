package com.g.sc;

public class GeographyLocation {
	private String latitude;
	private String longitude;
	private String locationGoogleName;
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getLocationGoogleName() {
		return locationGoogleName;
	}
	public void setLocationGoogleName(String locationGoogleName) {
		this.locationGoogleName = locationGoogleName;
	}
	
}
